package niit.com.backendshoppingcart.model;

public class User {
	
	private String User_id;
	private String User_password;
	private String User_name;
	private String User_mail;
	private String User_contact;
	private String User_role;
	private String getUser_id() {
		return User_id;
	}
	private void setUser_id(String user_id) {
		User_id = user_id;
	}
	private String getUser_password() {
		return User_password;
	}
	private void setUser_password(String user_password) {
		User_password = user_password;
	}
	private String getUser_name() {
		return User_name;
	}
	private void setUser_name(String user_name) {
		User_name = user_name;
	}
	private String getUser_mail() {
		return User_mail;
	}
	private void setUser_mail(String user_mail) {
		User_mail = user_mail;
	}
	private String getUser_contact() {
		return User_contact;
	}
	private void setUser_contact(String user_contact) {
		User_contact = user_contact;
	}
	private String getUser_role() {
		return User_role;
	}
	private void setUser_role(String user_role) {
		User_role = user_role;
	}
	
	

}
